package com.snet.unicode;

/**
 * this class will display the devanaagari form of provided unicode string
 * 
 * @author kalyan
 *
 */
public class UnicodeToDevanaagari {

	public static void main(String[] args) {
		String devanaagariScript = new String(
				"\u092e\u092e \u0928\u093e\u092e \u0915\u0932\u094d\u092f\u093e\u0923\u0903");
		System.out.println(devanaagariScript);
	}
}
